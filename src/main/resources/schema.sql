create table course (
    id bigint generated by default as identity,
    title varchar(255) not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table pay_session (
    id bigint generated by default as identity,
    session_status varchar(10) not null,
    amount bigint not null,
    maximum_students bigint not null,
    start_date timestamp not null,
    end_date timestamp not null,
    course_id bigint not null,
    foreign key (course_id) REFERENCES course(id),
    primary key(id)
);

create table free_session (
    id bigint generated by default as identity,
    session_status varchar(10) not null,
    start_date timestamp not null,
    end_date timestamp not null,
    course_id bigint not null,
    foreign key (course_id) REFERENCES course(id),
    primary key(id)
);

create table pay_session_image (
    id bigint generated by default as identity,
    image_path varchar(50) not null,
    width bigint not null,
    height bigint not null,
    image_size bigint not null,
    session_id bigint not null,
    foreign key (session_id) REFERENCES pay_session (id),
    primary key(id)
);

create table free_session_image (
    id bigint generated by default as identity,
    image_path varchar(50) not null,
    width bigint not null,
    height bigint not null,
    image_size bigint not null,
    session_id bigint not null,
    foreign key (session_id) REFERENCES free_session (id),
    primary key(id)
);

create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table pay_session_students (
    session_id bigint,
    student_id bigint,
    foreign key (session_id) REFERENCES pay_session (id),
    foreign key (student_id) REFERENCES ns_user (id),
    primary key (session_id, student_id)
);

create table free_session_students (
    session_id bigint,
    student_id bigint,
    foreign key (session_id) REFERENCES free_session (id),
    foreign key (student_id) REFERENCES ns_user (id),
    primary key (session_id, student_id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);


