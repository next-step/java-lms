
create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);


create table course (
    id bigint generated by default as identity,
    title varchar(255) not null,
    creator_id bigint not null,
    generation varchar(20) not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id),
    FOREIGN KEY (creator_id) REFERENCES ns_user (id)
);

create table image (
    id bigint generated by default as identity,
    original_file_name varchar(255) not null,
    image_type varchar(20) not null,
    cover_img_url varchar(255) not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);


create table session (
    id bigint generated by default as identity,
    title varchar(255) not null,
    description text,
    cover_image_id bigint,
    session_type varchar(20) not null,
    session_status varchar(20) not null,
    max_enrollment_size int,
    start_date_time timestamp,
    course_id bigint not null,
    end_date_time timestamp,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id),
    foreign key (cover_image_id) references image(id),
    foreign key (course_id) references course(id)
);

-- 기존 table column에 직접적으로 not null과 default 속성을 걸어둬도 , 기존의 데이터에는 영향을 받지 않고
-- 새롭게 추가되는 row에 대해서만 영향을 미친다.
-- 이러한 경우에는 alter table을 통해서 기본값 속성을 변경할 경우, 새롭게 추가된 column에 의해 null 값을 가지게 되는
-- 데이터들은 해당 alter table에서 정의한 속성을 가지게 할 수 있다.
alter table session
    add column session_recruitment_status varchar(20) not null default 'NOT_RECRUITING';


create table session_enrollment (
    id bigint generated by default as identity,
    session_id bigint not null,
    user_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id),
    foreign key (session_id) references session(id),
    foreign key (user_id) references ns_user(id)
);

