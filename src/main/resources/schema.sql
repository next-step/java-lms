create table course
(
    id         bigint generated by default as identity,
    title      varchar(255) not null,
    creator_id bigint       not null,
    created_at timestamp    not null,
    updated_at timestamp,
    primary key (id)
);

create table session
(
    id                 bigint generated by default as identity,
    course_id          bigint       not null,
    maximum_enrollment int          not null,
    period             varchar(255) not null,
    image_id           bigint       not null,
    opening_date_time  timestamp    not null,
    closing_date_time  timestamp    not null,
    session_status     varchar(255) not null,
    session_type       varchar(255) not null,
    created_at         timestamp    not null,
    updated_at         timestamp    not null,
    primary key (id)
);

create table image
(
    id         bigint generated by default as identity,
    name       varchar(255) not null,
    uri        varchar(255) not null,
    size       bigint       not null,
    image_type varchar(255) not null,
    created_at timestamp    not null,
    updated_at timestamp    not null,
    primary key (id)
);

create table session_next_step_user
(
    id         bigint generated by default as identity,
    session_id bigint    not null,
    user_id    bigint    not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id),
    unique (user_id, session_id)
);

create table next_step_user
(
    id         bigint generated by default as identity,
    user_id    varchar(20) not null,
    password   varchar(20) not null,
    name       varchar(20) not null,
    email      varchar(50),
    created_at timestamp   not null,
    updated_at timestamp,
    primary key (id)
);

create table question
(
    id         bigint generated by default as identity,
    created_at timestamp    not null,
    updated_at timestamp,
    contents clob,
    deleted    boolean      not null,
    title      varchar(100) not null,
    writer_id  bigint,
    primary key (id)
);

create table answer
(
    id          bigint generated by default as identity,
    created_at  timestamp not null,
    updated_at  timestamp,
    contents clob,
    deleted     boolean   not null,
    question_id bigint,
    writer_id   bigint,
    primary key (id)
);

create table delete_history
(
    id            bigint not null,
    content_id    bigint,
    content_type  varchar(255),
    created_date  timestamp,
    deleted_by_id bigint,
    primary key (id)
);
