create table course (
    id bigint generated by default as identity,
    title varchar(255) not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);

CREATE TABLE session (
    id	bigint	generated by default as identity,
    image_id	bigint	NOT NULL,
    pricing_type	varchar(4)	NOT NULL,
    tuition_fee	bigint	default 0 NULL,
    session_progress	varchar(20)	NOT NULL,
    session_recruitment	varchar(20)	NOT NULL,
    capacity	int default 0	NULL,
    start_date	timestamp	NULL,
    end_date	timestamp	NULL,
    created_at	timestamp	NOT NULL,
    updated_at	timestamp	NULL,
    primary key (id)
);

CREATE TABLE cover_image (
    id	bigint	generated by default as identity,
    name	varchar(30)	NOT NULL,
    extension	varchar(10)	NOT NULL,
    file_volume	bigint default 0	NOT NULL,
    width	int	default 0 NOT NULL,
    height	int	default 0 NOT NULL,
    created_at	timestamp	NOT NULL,
    updated_at	timestamp	NULL,
    primary key (id)
);

CREATE TABLE students (
    user_id	bigint	NOT NULL,
    session_id	bigint	NOT NULL,
    created_at	timestamp	NOT NULL,
    updated_at	timestamp	NULL,
    primary key (user_id, session_id)
);