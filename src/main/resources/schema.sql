create table course
(
    id         bigint generated by default as identity,
    title      varchar(255) not null,
    creator_id bigint       not null,
    created_at timestamp    not null,
    updated_at timestamp,
    primary key (id)
);

create table session
(
    id                  bigint generated by default as identity,
    course_id           bigint      not null,
    max_students        int         not null,
    current_students    int         not null,
    session_status_type varchar(20) not null,
    image_url           varchar(255),
    start_date          timestamp   not null,
    end_date            timestamp   not null,
    price               int         not null,
    created_at          timestamp   not null,
    updated_at          timestamp,
    primary key (id)
);

create table registration
(
    id                bigint generated by default as identity,
    course_id         bigint      not null,
    session_id        bigint      not null,
    ns_user_id        bigint      not null,
    registration_type varchar(20) not null,
    created_at        timestamp   not null,
    updated_at        timestamp,
    primary key (id)
);

create table ns_user
(
    id         bigint generated by default as identity,
    user_id    varchar(20) not null,
    password   varchar(20) not null,
    name       varchar(20) not null,
    email      varchar(50),
    created_at timestamp   not null,
    updated_at timestamp,
    primary key (id)
);

create table question
(
    id         bigint generated by default as identity,
    created_at timestamp    not null,
    updated_at timestamp,
    contents   clob,
    deleted    boolean      not null,
    title      varchar(100) not null,
    writer_id  bigint,
    primary key (id)
);

create table answer
(
    id          bigint generated by default as identity,
    created_at  timestamp not null,
    updated_at  timestamp,
    contents    clob,
    deleted     boolean   not null,
    question_id bigint,
    writer_id   bigint,
    primary key (id)
);

create table delete_history
(
    id            bigint not null,
    content_id    bigint,
    content_type  varchar(255),
    created_date  timestamp,
    deleted_by_id bigint,
    primary key (id)
);

update session
set session_status_type = 'IN_PROGRESS'
where session_status_type = 'RECRUITING';

alter table session
    add session_recruitment_type
        varchar(20) not null default '';

alter table ns_user
    add user_course_type
        varchar(20) not null default '';