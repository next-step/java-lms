create table course (
    id bigint generated by default as identity,
    title varchar(255) not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);

create table image (
    id bigint auto_increment,
    capacity bigint not null,
    image_type varchar(255) not null,
    width bigint not null,
    height bigint not null,
    primary key (id)
);

create table session_register_details (
    id bigint auto_increment,
    session_id bigint not null,
    max_count_of_students bigint not null,
    session_type varchar(255) not null,
    price bigint not null,
    session_status varchar(255) not null,
    primary key (id),
    foreign key (session_id) references session (id)
);

create table students (
    id bigint auto_increment,
    ns_user_id bigint not null,
    session_id bigint not null,
    primary key (id),
    foreign key (ns_user_id) references ns_user (id),
    foreign key (session_id) references session (id)
);

create table session (
    id bigint auto_increment,
    started_at timestamp not null,
    ended_at timestamp not null,
    session_name varchar(255) not null,
    image_id bigint not null,
    session_register_details_id bigint not null,
    primary key (id),
    foreign key (image_id) references image (id)
);
