create table course
(
    id         bigint generated by default as identity,
    title      varchar(255) not null,
    creator_id bigint       not null,
    created_at timestamp    not null,
    updated_at timestamp,
    primary key (id)
);

create table ns_user
(
    id         bigint generated by default as identity,
    user_id    varchar(20) not null,
    password   varchar(20) not null,
    name       varchar(20) not null,
    email      varchar(50),
    created_at timestamp   not null,
    updated_at timestamp,
    primary key (id)
);

create table question
(
    id         bigint generated by default as identity,
    created_at timestamp    not null,
    updated_at timestamp,
    contents   clob,
    deleted    boolean      not null,
    title      varchar(100) not null,
    writer_id  bigint,
    primary key (id)
);

create table answer
(
    id          bigint generated by default as identity,
    created_at  timestamp not null,
    updated_at  timestamp,
    contents    clob,
    deleted     boolean   not null,
    question_id bigint,
    writer_id   bigint,
    primary key (id)
);

create table delete_history
(
    id            bigint not null,
    content_id    bigint,
    content_type  varchar(255),
    created_date  timestamp,
    deleted_by_id bigint,
    primary key (id)
);

create table thumbnail
(
    thumbnail_id bigint generated by default as identity,
    name         varchar(255) not null,
    size         bigint       not null,
    width        int          not null,
    height       int          not null,
    created_at   timestamp    not null,
    updated_at   timestamp,
    writer_id    bigint,
    primary key (thumbnail_id)
);

create table session
(
    session_id      bigint generated by default as identity,
    name            varchar(255) not null,
    start_date      date         not null,
    end_date        date         not null,
    thumbnail_id    bigint       not null,
    session_type_id bigint       not null,
    session_status  varchar(20)  not null
        created_at timestamp not null,
    updated_at      timestamp,
    writer_id       bigint,
    primary key (session_id)
);

create table session_type_free
(
    session_type_id bigint generated by default as identity,
    created_at      timestamp not null,
    updated_at      timestamp,
    writer_id       bigint,
    primary key (session_type_id)
);

create table session_type_paid
(
    session_type_id bigint generated by default as identity,
    max_students    int       not null,
    session_fee     int       not null,
    created_at      timestamp not null,
    updated_at      timestamp,
    writer_id       bigint,
    primary key (session_type_id)
);
