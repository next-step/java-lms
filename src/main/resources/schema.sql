create table course (
    id bigint generated by default as identity,
    title varchar(255) not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);

create table session (
    id bigint generated by default as identity
    , start_date date
    , end_date date
    , session_state varchar(50)
    , session_recruiting_state varchar(50)
    , session_paid_type varchar(50)
    , registered_student int
    , student_capacity int
    , image_id bigint
    , primary key (id)
);

create table student (
    ns_user_id bigint not null
    , session_id bigint not null
    , register_type varchar(50) not null
    , created_at timestamp default current_timestamp
    , updated_at timestamp default current_timestamp on update current_timestamp
    , primary key (ns_user_id, session_id)
);