create table course
(
    id         bigint generated by default as identity,
    title      varchar(255) not null,
    creator_id bigint       not null,
    created_at timestamp    not null,
    updated_at timestamp,
    primary key (id)
);

create table ns_user
(
    id         bigint generated by default as identity,
    user_id    varchar(20) not null,
    password   varchar(20) not null,
    name       varchar(20) not null,
    email      varchar(50),
    created_at timestamp   not null,
    updated_at timestamp,
    primary key (id)
);

create table question
(
    id         bigint generated by default as identity,
    created_at timestamp    not null,
    updated_at timestamp,
    contents   clob,
    deleted    boolean      not null,
    title      varchar(100) not null,
    writer_id  bigint,
    primary key (id)
);

create table answer
(
    id          bigint generated by default as identity,
    created_at  timestamp not null,
    updated_at  timestamp,
    contents    clob,
    deleted     boolean   not null,
    question_id bigint,
    writer_id   bigint,
    primary key (id)
);

create table delete_history
(
    id            bigint not null,
    content_id    bigint,
    content_type  varchar(255),
    created_date  timestamp,
    deleted_by_id bigint,
    primary key (id)
);

create table lecture
(
    id                        bigint generated by default as identity,
    title                     varchar(255) not null,
    lecture_type              varchar(50)  not null,
    lecture_status            varchar(50)  not null,
    lecture_recruiting_status varchar(50)  not null,
    started_at                timestamp    not null,
    ended_at                  timestamp,
    price                     bigint       not null default 0,
    limit_student_count       bigint       not null default 0,
    created_at                timestamp    not null,
    updated_at                timestamp,
    primary key (id)
);

create table cover_image
(
    id               bigint generated by default as identity,
    name             varchar(255) not null,
    image_file_size  bigint       not null,
    cover_image_type varchar(20),
    width            bigint       not null,
    height           bigint       not null,
    created_at       timestamp    not null,
    updated_at       timestamp,
    primary key (id)
);

create table cover_image_lecture_mapping
(
    id             bigint generated by default as identity,
    lecture_id     bigint    not null,
    cover_image_id bigint    not null,
    created_at     timestamp not null,
    updated_at     timestamp,
    primary key (id)
);
